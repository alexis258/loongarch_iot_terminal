//硬件IO口初始化
void InitIo(void)
{
    P0SEL &= ~0x01; //让P0.0为普通IO口，
    P0DIR |= 0x01; //让P0.0为为输出

    P1SEL &= ~0x8C; //让 P1.2 P1.3 P1.7为普通IO口
    P1DIR |= 0x8C; //把 P1.2 P1.3 1.7设置为输出  
}

