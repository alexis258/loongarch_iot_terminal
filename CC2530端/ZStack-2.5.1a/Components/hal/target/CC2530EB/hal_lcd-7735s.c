//向SPI总线传输一个8位数据
void  SPI_WriteData(u8 Data)
{
    unsigned char i=0;

    for(i=8;i>0;i--)
    {
        if(Data&0x80)	
        {
            LCD_SDA_SET; //输出数据
        }
        else
        {
            LCD_SDA_CLR;
        }

        LCD_SCL_CLR;
        LCD_SCL_SET;
        Data<<=1; 
    }
}

//向液晶屏写一个8位指令
void Lcd_WriteIndex(u8 Index)
{
    LCD_RS_CLR;
    SPI_WriteData(Index);
}

